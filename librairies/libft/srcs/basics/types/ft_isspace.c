#include "libft.h"

int		ft_isspace(int c)
{
	if (c == ' ')
		return (1);
	if (c == '\v' || c == '\f' || c == '\r')
		return (2);
	if (c == '\t' || c == '\n')
		return (3);
	return (0);
}
